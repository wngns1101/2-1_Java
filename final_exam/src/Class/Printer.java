package Class;

public class Printer {
	
	public static void println(int x) {
		System.out.println(x);
	}
	public static void println(boolean x) {
		System.out.println(x);
	}
	public static void println(double x) {
		System.out.println(x);
	}
	public static void println(String x) {
		System.out.println(x);
	}
	
}
